const maskCreditCard = function (number) {
  const str = number + '';
  const last = str.slice(-4);
  return last.padStart(str.length, '*');
};

console.log(maskCreditCard(32486242452));
console.log(maskCreditCard(7563234235457452475613));
console.log(maskCreditCard('2893645263718479341342'));

